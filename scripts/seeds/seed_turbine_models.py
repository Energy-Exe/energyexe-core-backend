def seed_turbine_models(db: Session):
    """Seed turbine_models table with initial data"""
    print(f"  Checking for existing turbine models...")

    # Get existing model names
    existing_models = {t.model for t in db.query(TurbineModel.model).all()}

    # Filter out models that already exist
    models_to_add = []
    skipped_count = 0

    for model_data in TURBINE_MODELS_DATA:
        if model_data["model"] in existing_models:
            skipped_count += 1
            continue

        models_to_add.append(TurbineModel(**model_data))

    if not models_to_add:
        print(f"  Found {len(existing_models)} existing models, no new models to add")
        if skipped_count > 0:
            print(f"  Skipped {skipped_count} existing models")
        return

    print(f"  Adding {len(models_to_add)} new turbine models...")
    if skipped_count > 0:
        print(f"  Skipping {skipped_count} existing models")

    db.add_all(models_to_add)
    db.commit()

    print(f"  Successfully added {len(models_to_add)} turbine models")
